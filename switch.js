// Días de la semana
var day = "Domingo";

switch (day) {
  case "Lunes":
    console.log("Ir a trabajar");
    break;
  case "Martes":
    console.log("Reunión de equipo");
    break;
  case "Miércoles":
    console.log("Proyecto de desarrollo");
    break;
  case "Jueves":
    console.log("Presentación de informe");
    break;
  case "Viernes":
    console.log("Prepararse para el fin de semana");
    break;
  case "Sábado":
    console.log("Disfrutar del tiempo libre");
    break;
  case "Domingo":
    console.log("Relajarse y descansar");
    break;
  default:
    console.log("No existe ese día");
    break;
}